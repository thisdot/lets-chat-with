/**
 * Report views to transition to based on reporting state.
 *
 * Reasons - display options for reporting.
 * OtherReason - display option for submitting a message with the report request.
 * SuccessMessage - display the success message for a submitted report.
 *
 * @export
 * @enum {number}
 */
export enum ReportView {
  Reasons,
  OtherReason,
  SuccessMessage,
}
